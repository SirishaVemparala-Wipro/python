set1={1,2,3,4,5,6}

set2={4,5,6,7,8,9}

# elements from both sets
union_set=set1 | set2

#common elements
intersection_set=set1 & set2

#elements in set1 but not in set2
difference_set=set1-set2

print("Set 1: ",set1)

print("Set 2: ",set2)

print("Union: ",union_set)

print("Intersection: ",intersection_set)

print("Difference: ",difference_set)
